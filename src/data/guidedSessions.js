// Function to initialize guided sessions (would be called from admin route)
export const initializeGuidedSessions = async () => {
  try {
    const response = await fetch('/api/meditation/guided/init', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${localStorage.getItem('token')}`
      },
      body: JSON.stringify({ sessions: defaultGuidedSessions })
    })
    
    if (response.ok) {
      console.log('Guided sessions initialized successfully')
    }
  } catch (error) {
    console.error('Error initializing guided sessions:', error)
  }
}
